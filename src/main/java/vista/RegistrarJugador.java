    private void establecerAvatar(ImageIcon avatar, String nombreAvatar) {
        this.avatarSelect.setIcon(avatar);
        this.avatarSeleccionado = nombreAvatar;
    }
    
    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        String mensaje = "";
        if(avatarSelect.getIcon() == null){
            mensaje += "Selecciona avatar del Jugador\r\n";
        }
        
        if(txtNomJugador.getText().equalsIgnoreCase("")){
            mensaje += "Introduce nombre del Jugador\r\n";
        }
        
        if (!mensaje.equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, mensaje, "Registro de jugador", JOptionPane.INFORMATION_MESSAGE);
        } else{
            Jugador jugador = new Jugador(this.txtNomJugador.getText(), this.avatarSeleccionado);
            PreferenciaColor pref = new PreferenciaColor("#FF0000", "#0000FF", "#00FF00");
            jugador.setPreferenciaColor(pref);
            jugador.setColor("#0F0F0");
            menu.setJugador(jugador);
            this.dispose();
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

